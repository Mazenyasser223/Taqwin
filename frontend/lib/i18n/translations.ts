export const translations: Record<AppLanguage, Record<TranslationKey, string>> = { en, ar };

export function translate(language: AppLanguage, key: TranslationKey, params?: Record<string, string>): string {
  let text = translations[language][key] ?? translations.en[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      text = text.replace(`{${k}}`, v);
    }
  }
  return text;
}
